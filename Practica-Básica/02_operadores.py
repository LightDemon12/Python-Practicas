# Operadores en python y alguno usos basicos

#se pueden hacer operaciones matematicas basicas usando un print

print(2+2) #suma
print(3-2) #resta
print(3*2) #multiplicacion
print(3/2) #division
print(3%2) #modulo
print(3**2) #exponente-- 3 al cuadrado
print(3//2) #division entera
print(3+2+3-2/4) #se pueden hacer operaciones combinadas

#operaciones mixtas

print("HOLA"*2) #se puede multiplicar un string
print("HOLA"+"MUNDO") #se pueden concatenar strings
print("HOLA"+str(2)) #se pueden concatenar strings con un numero convertido a string
print ("HOLA"*(2**3)) #se puede multiplicar un string por un numero grande

#operadores de comparacion, devuelven un booleano

print(3==2) #igual a
print(3!=2) #diferente de ( SE ESCRIBE ASI! =)
print(3>2) #mayor que
print(3<2) #menor que
print(3>=2) #mayor o igual que( SE ESCRIBE ASI = >)
print(3<=2) #menor o igual que( SE ESCRIBE ASI = <)
